# Function to save dataset
def save_to_csv(file_name, data):
    with open(file_name, 'w', newline='') as csvfile:
        writer = csv.writer(csvfile)
        writer.writerow(['plaintext', 'ciphertext', 'algorithm', 'key'])
        for row in data:
            writer.writerow(row)

# Encryption functions
def encrypt_with_chacha20(plaintext):
    key = get_random_bytes(32)
    cipher = ChaCha20.new(key=key)
    ciphertext = cipher.encrypt(plaintext.encode())
    return ciphertext.hex(), key.hex(), 'ChaCha20'

def encrypt_with_salsa20(plaintext):
    key = get_random_bytes(32)
    cipher = Salsa20.new(key=key)
    ciphertext = cipher.encrypt(plaintext.encode())
    return ciphertext.hex(), key.hex(), 'Salsa20'

# Generate dataset
def generate_dataset():
    dataset = []
    for sentence in sentences:
        dataset.append((sentence, *encrypt_with_chacha20(sentence)))
        dataset.append((sentence, *encrypt_with_salsa20(sentence)))
    save_to_csv('stream_ciphers_dataset.csv', dataset)

if __name__ == "__main__":
    generate_dataset()
