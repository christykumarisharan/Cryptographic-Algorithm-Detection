# Hill Cipher (with random 2x2 matrix key)
def hill_cipher(plaintext):
    key_matrix = np.random.randint(1, 26, (2, 2))
    
    # Pad plaintext if odd number of characters
    if len(plaintext) % 2 != 0:
        plaintext += 'X'
    
    # Convert plaintext to pairs of characters
    ciphertext = ''
    for i in range(0, len(plaintext), 2):
        pair = plaintext[i:i+2]
        vec = [(ord(char.upper()) - 65) for char in pair]  # Convert pair to numerical values
        result = np.dot(key_matrix, vec) % 26  # Perform matrix multiplication and mod 26
        ciphertext += ''.join([chr(num + 65) for num in result])  # Convert back to letters

    key = f"matrix={key_matrix.tolist()}"
    return ciphertext, key, 'Hill Cipher'

