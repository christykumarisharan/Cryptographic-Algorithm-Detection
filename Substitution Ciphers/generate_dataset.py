# Caesar Cipher (with random shift for each plaintext)
def caesar_cipher(plaintext):
    shift = random.randint(1, 25)  # Random shift between 1 and 25
    ciphertext = ''.join([chr((ord(char) - 65 + shift) % 26 + 65) if char.isupper() 
                          else chr((ord(char) - 97 + shift) % 26 + 97) if char.islower() 
                          else char for char in plaintext])
    return ciphertext, f"shift={shift}", 'Caesar Cipher'

# Vigenère Cipher (with random key for each plaintext)
def vigenere_cipher(plaintext):
    key_length = random.randint(3, 8)  # Random key length between 3 and 8
    key = ''.join(random.choice(string.ascii_uppercase) for _ in range(key_length))
    ciphertext = ''.join([chr((ord(char) - 65 + ord(key[i % len(key)]) - 65) % 26 + 65) if char.isupper() 
                          else chr((ord(char) - 97 + ord(key[i % len(key)]) - 97) % 26 + 97) if char.islower() 
                          else char for i, char in enumerate(plaintext)])
    return ciphertext, f"key={key}", 'Vigenère Cipher'

# Affine Cipher (with random keys for each plaintext)
def affine_cipher(plaintext):
    def gcd(a, b):
        while b != 0:
            a, b = b, a % b
        return a
    
    while True:
        a = random.randint(1, 25)
        if gcd(a, 26) == 1:
            break
    b = random.randint(0, 25)
    ciphertext = ''.join([chr(((a * (ord(char) - 65) + b) % 26) + 65) if char.isupper() 
                          else chr(((a * (ord(char) - 97) + b) % 26) + 97) if char.islower() 
                          else char for char in plaintext])
    return ciphertext, f"a={a}, b={b}", 'Affine Cipher'

